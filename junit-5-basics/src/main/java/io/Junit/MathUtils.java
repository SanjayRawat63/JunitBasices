package io.Junit;

public class MathUtils {
	
        public int add(int a,int b)
        {
        	return a+b;
        }
        public int sub(int a,int b)
        {
        	return a-b;
        }
        public int mul(int a,int b)
        {
        	return a*b;
        }
        public int divide(int a,int b)
        {
        	return a/b;
        }
}
